  function t(key, params) {
    const pack = STRINGS[BarcoderI18n.locale] || STRINGS[DEFAULT_LOCALE];
    const fb = STRINGS[DEFAULT_LOCALE];
    let s = pack[key] != null ? pack[key] : fb[key] != null ? fb[key] : key;
    if (params) {
      Object.keys(params).forEach((k) => {
        s = s.replace(new RegExp("\\{" + k + "\\}", "g"), String(params[k]));
      });
    }
    return s;
  }

  function applyStatic() {
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-html]").forEach((el) => {
      el.innerHTML = t(el.getAttribute("data-i18n-html"));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.title = t(el.getAttribute("data-i18n-title"));
    });
    document.querySelectorAll("option[data-i18n]").forEach((el) => {
      el.textContent = t(el.getAttribute("data-i18n"));
    });
    const titleEl = document.querySelector("title");
    if (titleEl) titleEl.textContent = t("meta.title");
    document.documentElement.lang = BarcoderI18n.locale;
    syncLangButtons();
  }

  function syncLangButtons() {
    const loc = BarcoderI18n.locale;
    document.querySelectorAll(".lang-btn[data-locale]").forEach((btn) => {
      const on = btn.getAttribute("data-locale") === loc;
      btn.classList.toggle("active", on);
      btn.setAttribute("aria-pressed", on ? "true" : "false");
    });
  }

  const BarcoderI18n = {
    locale: DEFAULT_LOCALE,
    t: t,
    apply: applyStatic,
    setLocale(loc) {
      if (!STRINGS[loc]) loc = DEFAULT_LOCALE;
      BarcoderI18n.locale = loc;
      try {
        localStorage.setItem(LOCALE_KEY, loc);
      } catch (e) {}
      applyStatic();
      if (typeof BarcoderI18n.onChange === "function") BarcoderI18n.onChange();
    },
    init() {
      let saved = DEFAULT_LOCALE;
      try {
        saved = localStorage.getItem(LOCALE_KEY) || DEFAULT_LOCALE;
      } catch (e) {}
      if (!STRINGS[saved]) saved = DEFAULT_LOCALE;
      BarcoderI18n.locale = saved;
      const bar = document.getElementById("barcoder-lang-bar");
      if (bar) {
        bar.querySelectorAll(".lang-btn[data-locale]").forEach((btn) => {
          btn.addEventListener("click", () => {
            const loc = btn.getAttribute("data-locale");
            if (loc && loc !== BarcoderI18n.locale) BarcoderI18n.setLocale(loc);
          });
        });
      }
      applyStatic();
    },
  };

  global.BarcoderI18n = BarcoderI18n;
